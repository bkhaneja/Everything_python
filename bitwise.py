# binary numbers
a = 0b1001
b = 0b1100

# prints the decimal equivalent
print(a)

# prints the binary 
print(bin(a)) 

# bitwise OR operator
print(bin(a | b))

# bitwise AND operator
print(bin(a & b))

# bitwise XOR operator
print(bin(a ^ b))

# shift operator right shift
print(bin(a >> 2))

#shift operator left shift
print(bin(a << 4))